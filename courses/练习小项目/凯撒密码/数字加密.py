# 数字密码加密程序
# 将输入的多位数字密码，每一位都往后挪 x 位

x = 3  # 往后挪多少位

# 输入密码（字符串形式，如 "123456"）
原来的密码 = input("请输入数字密码：")

# 用于存储加密后的结果
加密后的密码 = ""

# 遍历每一位数字
for 带引号的数字 in 原来的密码:
    # 把字符串数字转换成整数
    数字 = int(带引号的数字)

    # 往后挪 x 位
    新数字 = 数字 + x

    # 取余，确保结果在 0-9 之间
    新数字 = 新数字 % 10

    # 把结果加到加密后的密码中
    加密后的密码 = 加密后的密码 + str(新数字)

# 输出加密后的密码
print("加密后的密码：", 加密后的密码)
